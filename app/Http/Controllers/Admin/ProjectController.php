     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();

        $newCar = new Project();
        $newCar->fill($data);
        $newCar->save();
        $newCar->tags()->attach($request->tags);
        return redirect()->route("admin.cars.index");

    }

